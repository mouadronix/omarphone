-- OmarPhone full database reset.
-- WARNING: this deletes all existing data in the listed OmarPhone tables.
-- Run this in Neon/Postgres SQL Editor when you want a fresh production database.
--
-- Image uploads:
--   1. POST /api/media with admin auth and JSON:
--      {"filename":"photo.png","contentType":"image/png","data":"data:image/png;base64,..."}
--   2. The API returns {"media":{"url":"/api/media/<id>"}}.
--   3. Put that URL in any image column, for example service_cards.image,
--      blog_posts.image, customer_reviews.avatar, before_after_repairs.before_image.
--   4. The frontend <img> tags already render /api/media/<id> URLs.

