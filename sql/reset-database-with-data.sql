CREATE TABLE orders (
  id TEXT PRIMARY KEY,
  created_at TIMESTAMPTZ NOT NULL,
  server_received_at TIMESTAMPTZ NOT NULL,
  status TEXT NOT NULL DEFAULT 'New',
  payload_json JSONB NOT NULL
